$.fn.toastr = function(options, cb) {
   var defaults = {
      notifications: [],
      notificationTypes: {
         success: {
            icon: 'check'
         },
         warning: {
            icon: 'warning'
         },
         error: {
            icon: 'close'
         }
      },
      hidingTimer: 5000,
      maxItems: 5,
      autoClose: $(this).data('autoclose'),
      mobileAnimation: false,
      animationSpeed: 500,
      invertedStyling: false,
      onClose: function() {}
   };
   var settings = $.extend({}, defaults, options);
   var $notifications = $(this);
   var hideToastr = function($toastr) {
      $toastr.data('close', 'true');
      refreshToastrs();
      settings.onClose();
   };
   var registerToastrCloser = function($toastr) {
      var $closer = $toastr.find('.js-toastr-closer');
      $closer.on('click', function() {
         hideToastr($toastr);
      });
   };
   var prepareNotification = function() {
      var $item = $('#js-toastr-prototype').clone().removeAttr('id').uniqueId().removeClass('d-none').appendTo($notifications);
      if (settings.invertedStyling) {
         $item.addClass('invert').find('.toastr-item').addClass('invert');
      }
      return $item;
   };
   var refreshToastrs = function() {
      var top = 0;
      $('.toastr-collection .toastr').each(function(index, toastr) {
         var $toastr = $(toastr);
         if (index === 0) {
            top = getVisibleHeightOfHeader() + getVisibleHeightOfHeaderNavigation() + getHeightOfSellerDashboardHeader() + (getNotificationBarHeader() > 0 && getHeightOfSellerDashboardHeader() > 0 ? getNotificationBarHeader() - getHeightOfSellerDashboardHeader() : getNotificationBarHeader());
         }
         if ($toastr.data('shown') == null) {
            if (!settings.mobileAnimation && c24.tools.responsiveSize() == 'xs') {
               $toastr.hide().css('top', top + 'px').css('left', 0).css('right', 0).fadeIn('slow').data('shown', true);
            } else {
               $toastr.css('top', top + 'px').css('right', -1.1 * $toastr.width() + 'px').animate({
                  right: 0,
                  left: c24.tools.responsiveSize() == 'xs' ? '0px' : 'auto',
                  opacity: 1
               }, settings.animationSpeed).data('shown', true);
            }
         } else {
            $toastr.animate({
               top: top + 'px'
            }, {
               duration: settings.animationSpeed,
               queue: false
            });
         }
         if (settings.autoClose || $toastr.data('close')) {
            setTimeout(function() {
               $toastr.animate({
                  opacity: 0
               }, {
                  duration: settings.animationSpeed,
                  complete: function() {
                     $toastr.remove();
                     refreshToastrs();
                     settings.onClose();
                  }
               });
            }, !$toastr.data('close') ? settings.hidingTimer : 0);
         }
         top += $toastr.outerHeight() + 10;
      });
   };
   var processToastrItems = function() {
      var toastrItems = settings.notifications;
      if (toastrItems.length > 0) {
         $.each(toastrItems, function(index, value) {
            if (index <= settings.maxItems - 1) {
               var $toastr = prepareNotification();
               var iconClass = 'i-' + (typeof value.icon !== 'undefined' ? value.icon : settings.notificationTypes[value.type].icon);
               $toastr.find('.toastr-item').addClass(value.type);
               $toastr.find('.js-toastr-icon i').addClass(iconClass);
               $toastr.find('.js-toastr-text').html(value.text);
               registerToastrCloser($toastr);
            }
         });
         refreshToastrs();
      }
   };
   processToastrItems();
   if (typeof cb != 'undefined') {
      cb();
   }
};
