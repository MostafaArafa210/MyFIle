const getVisibleHeightOfElem = $el => {
   if ($el.length > 0) {
      var elemHeight = $el.outerHeight();
      var windowHeight = $(window).height();
      var rect = $el[0].getBoundingClientRect();
      var rectTop = rect.top;
      var rectBottom = rect.bottom;
      return Math.max(0, rectTop > 0 ? Math.min(elemHeight, windowHeight - rectTop) : Math.min(rectBottom, windowHeight));
   } else {
      return 0;
   }
};
